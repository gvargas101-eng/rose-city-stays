export const getBlogArticlesByCategory = (category: string): BlogArticle[] => {
  return blogArticles
    .filter((article) => article.category === category)
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime());
};

export const getRelatedArticles = (articleId: string): BlogArticle[] => {
  const article = getBlogArticleById(articleId);
  if (!article) return [];
  return article.relatedArticles
    .map((id) => getBlogArticleById(id))
    .filter((a) => a !== undefined) as BlogArticle[];
};
